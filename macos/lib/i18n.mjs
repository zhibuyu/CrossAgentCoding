let current = 'zh';

export function setLang(lang) {
  if (TEXT[lang]) current = lang;
}

export function getLang() {
  return current;
}

export function langCycle() {
  const order = ['zh', 'en', 'zh-TW'];
  const idx = order.indexOf(current);
  return order[(idx + 1) % order.length];
}

// All strings for the current language (shipped to the web GUI front-end so it
// can localize labels client-side without a round-trip per key).
export function allStrings() {
  return { ...(TEXT.zh), ...(TEXT[current] || {}) };
}

// Translate a key with optional positional {0},{1}… substitution.
export function t(key, ...args) {
  const table = TEXT[current] || TEXT.zh;
  let s = table[key] != null ? table[key] : (TEXT.zh[key] != null ? TEXT.zh[key] : key);
  args.forEach((v, i) => {
    s = s.replace(new RegExp(`\\{${i}\\}`, 'g'), String(v));
  });
  return s;
}
